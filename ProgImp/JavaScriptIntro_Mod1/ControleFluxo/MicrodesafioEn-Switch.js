//Estações do ano com Switch
const estacao = "Primavera";

switch(estacao){
    case "Verao": //Compara se variavel com os case, se true retona mensgem no console
    console.log("Que calor hein?!");
    break
    case "Inverno":
    console.log("Frio dms!");  
    break
    case "Outono":
    console.log("É a estação em que as folhas caem");
    break
    case "Primavera":
    console.log("O momento em que as flores crescem")      
}











